export function t(lang, key) {
  const keys = key.split(".");
  let v = translations[lang] ?? translations[defaultLang];
  for (const k of keys) {
    v = v?.[k];
    if (v == null) {
      const fallback = translations[defaultLang];
      let w = fallback;
      for (const kk of keys) {
        w = w?.[kk];
        if (w == null) return key;
      }
      return typeof w === "string" ? w : key;
    }
  }
  return typeof v === "string" ? v : key;
}

export function replaceParams(str, params) {
  if (typeof str !== "string") return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => (params[k] != null ? String(params[k]) : `{${k}}`));
}
